package com.example;

public class LoopsMethods {
    public static void LoopFor() {
        for (int i = 0; i < 4; i++) {
            System.out.println("Iteration: " + i);
        }
        
        for (int a = 5, b = 1; a > b; a--, b++) {
            System.out.println("a: " + a + ", b: " + b);
        }
    }
    
}
